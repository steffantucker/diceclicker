function Stats() {
  // Total number of rolls (hand + machine)
  this.rollTotal = 0;
  // Number of hand rolls (clicks, not dice)
  this.handRolls = 0;
  // Number of ring spins
  this.ringSpins = 0;
  // Number of times machines have rolled (not dice)
  this.machineRolls = 0;
  // Number of dice sold
  this.diceSold = 0;
  // Number of dice bought
  this.diceBought = 0;
  // Number of dicebag upgrades bought
  this.diceBagUpgradesBought = 0;
  // Number of dicebag upgrades sold
  this.diceBagUpgradesSold = 0;
  // Number of machines bought
  this.machinesBought = 0;
  // Number of machines sold
  this.machinesSold = 0;
  // Number of cups bought
  this.cupsBought = 0;
  // Number of cups sold
  this.cupsSold = 0;
  // Number of rings bought
  this.ringsBought = 0;
  // Number of rings sold
  this.ringsSold = 0;
  // Total pips of all dice rolled
  this.totalPips = 0;
  // Number of dice that have gotten stuck
  this.numDiceStuck = 0;
  // Number of dice cheated
  this.numCheats = 0;
}
